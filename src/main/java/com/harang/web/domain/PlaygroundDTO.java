package com.harang.web.domain;

public class PlaygroundDTO {
	String pg_num;
	String pg_type;
	String pg_name;
	String pg_content;
	String pg_image;
	int pg_point;
	public String getPg_num() {
		return pg_num;
	}
	public void setPg_num(String pg_num) {
		this.pg_num = pg_num;
	}
	public String getPg_type() {
		return pg_type;
	}
	public void setPg_type(String pg_type) {
		this.pg_type = pg_type;
	}
	public String getPg_name() {
		return pg_name;
	}
	public void setPg_name(String pg_name) {
		this.pg_name = pg_name;
	}
	public String getPg_content() {
		return pg_content;
	}
	public void setPg_content(String pg_content) {
		this.pg_content = pg_content;
	}
	public String getPg_image() {
		return pg_image;
	}
	public void setPg_image(String pg_image) {
		this.pg_image = pg_image;
	}
	public int getPg_point() {
		return pg_point;
	}
	public void setPg_point(int pg_point) {
		this.pg_point = pg_point;
	}
}
